<template>
  <div>
    <navigation />
    <item-header />
    <app-content>
      <section>
        <div class="container">
          <!-- Filters -->
          <div class="filter">
            <div class="type">
              <span :class="{ active: type === 'reagentFor' }" class="btn-subtle" @click="setType('reagentFor')">
                Reagent for <span class="btn-counter">{{ crafting.reagentFor ? crafting.reagentFor.length : 0 }}</span>
              </span>
              <span :class="{ active: type === 'createdBy' }" class="btn-subtle" @click="setType('createdBy')">
                Created by <span class="btn-counter">{{ crafting.createdBy ? crafting.createdBy.length : 0 }}</span>
              </span>
            </div>
            <div class="filter-tags">
              <div class="filter-tag-row">
                <!-- Filters -->
                <div v-for="filter in filters" :key="filter.name" :class="{ active: filter.active, descending: filter.descending }" class="btn-tag" @click="selectFilterTag(filter)">
                  <img v-if="filter.icon" :src="filter.icon" :alt="filter.name" class="ico-12">
                  <span>{{ filter.name }}</span>
                  <img :class="{ descending: filter.descending }" src="/img/ui/dropdown.svg" class="ico-16 asc-desc" alt="Ascending/Descending">
                </div>
              </div>
            </div>
          </div>

          <!-- All Orders-->
          <div class="row labels">
            <div class="col item">
              Item
            </div>
            <div class="col user">
              Amount
            </div>
            <div class="col interactive quantity" @click="selectFilterTag(filters.find(f => f.name === 'Quantity'))">
              Market Value
            </div>
            <div class="col interactive price" @click="selectFilterTag(filters.find(f => f.name === 'Price'))">
              Profit
            </div>
            <div class="col interactive whitespace" />
          </div>
          <transition-group>
            <div v-for="(entry, i) in crafting[type]" :key="entry.itemId + '-' + i">
              <crafting-entry :crafting="entry" :cid="entry.itemId + '-' + i" />
            </div>
          </transition-group>
        </div>
      </section>
    </app-content>
  </div>
</template>



<script>
import appContent from 'src/app-content.vue'
import craftingEntry from 'src/components/wow-classic/crafting-entry.vue'
import itemHeader from 'src/components/wow-classic/header.vue'
import navigation from 'src/components/ui/nav/wow-classic.vue'
import storeModule from 'src/store/wow-classic/crafting.js'

export default {
  components: {
    appContent,
    craftingEntry,
    navigation,
    itemHeader
  },

  async asyncData ({ store, route }) {
    const item = route.params.item
    const slug = route.params.slug

    // Only fetch item data if we actually have a new item or new server
    if (store.state.crafting.itemId !== parseInt(item) || store.state.crafting.slug !== slug) {
      const craftingData = await this.$cubic.get(`/wow-classic/v1/crafting/${slug}/${item}`)
      store.commit('setCrafting', craftingData)
    }
  },

  computed: {
    item () {
      return this.$store.state.items.item
    },
    crafting () {
      // Populate createdBy with item details
      const createdBy = this.$store.state.crafting.createdBy
      for (const entry of createdBy) {
        entry.itemId = this.item.itemId
        entry.name = this.item.name
        entry.icon = this.item.icon
        entry.marketValue = this.item.stats.current.marketValue
      }

      return {
        createdBy,
        reagentFor: this.$store.state.crafting.reagentFor
      }
    },
    type () {
      return this.$store.state.crafting.type
    },
    filters () {
      const filters = []
      const entries = this.crafting[this.type]
      for (const entry of entries) {
        if (filters.find((o) => o.name === entry.category)) continue

        // TODO: Replace this once api endpoint is there
        filters.push({
          name: entry.category,
          icon: `https://wow.zamimg.com/images/wow/icons/small/trade_${entry.category.toLowerCase()}.jpg`
        })
      }
      return filters
    }
  },

  methods: {
    setType (type) {
      if (type !== this.type) this.$store.commit('setOrderType', type)
    }
  },

  head () {
    return {
      title: `${this.item.name} Â· NexusHub`
    }
  },

  storeModule
}
</script>



